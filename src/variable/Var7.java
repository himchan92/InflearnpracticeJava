package variable;

public class Var7 {

    public static void main(String[] args) {
        int a = 100; //정수
        double b = 10.5; //실수
        boolean c = true; //참거짓
        char d = 'A'; // 문자는 ''로 감싸야 함
        String e = "hello java"; //문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
